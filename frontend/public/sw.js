// Push notifications
self.addEventListener('push', (event) => {
  const options = {
    body: event.data ? event.data.text() : 'New update from VibeCaaS',
    icon: '/VibeCaaS.png',
    badge: '/VibeCaaS.png',
    vibrate: [100, 50, 100],
    data: {
      dateOfArrival: Date.now(),
      primaryKey: 1
    },
    actions: [
      {
        action: 'explore',
        title: 'Go to the site',
        icon: '/VibeCaaS.png'
      },
      {
        action: 'close',
        title: 'Close notification',
        icon: '/VibeCaaS.png'
      }
    ]
  };

  event.waitUntil(
    self.registration.showNotification('VibeCaaS', options)
  );
});

// Handle notification clicks
self.addEventListener('notificationclick', (event) => {
  event.notification.close();

  if (event.action === 'explore') {
    // Open the app
    event.waitUntil(
      clients.openWindow('/')
    );
  }
});

